
// Interface is like a class or a blueprint of a class where it can have methods and variables but the
// methods declared in interface are by default abstract(Only method signature,no body)
// Interface specify what a class must do and not how.
// Interface is about capabilities like a PHONE may be an interface and any class like NOKIA, MOTOROLA, GOOGLE
// has to implement a set of methods specifies by PHONE interface
// Since java does not support multiple inheritance in case of class, but by using interface it can achieve multiple inheritance .
// Since Java 8, we can have default and static methods in an interface.
// Since Java 9, we can have private methods in an interface. 

// Why use interfaces when we have abstract classes?
// -- The reason is, abstract classes may contain non-final variables, whereas variables in interface are final, public and static.